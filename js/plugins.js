(function($) {
	$.fn.dynamicInputLabels = function() {
		return this.each(function() {

			var targetInput = $(this);
			var targetName = $(this).attr('name');
			var targetLabel = $(this).parents('.input-holder').find('label');

			targetInput.focus(function(){
				if ( !targetInput.val() ) {
					targetLabel.fadeTo('fast', 0.5);
				}
			});

			targetInput.keydown(function(){
					targetLabel.hide();
			});

			targetInput.blur(function(){
				if ( !targetInput.val() ) {
					targetLabel.fadeTo('fast', 1.0);
				}
			});
		});
	}
})(jQuery);

